    <h1>Seidu Ramatu</h1>
    <p>
      Did you know that most women living in rural areas in Ghana, especially
      those with no educational background, do not have access to financial
      support to work their way out of poverty?
    </p>
    <p>
      I will never forget Seidu Ramatu, a food vendor in Garu (a small village
      in the Upper East region of Ghana). She is 67 years old and has been a
      food vendor for 15 years. Ramatu is married to a husband who cannot work
      due to sickness from old age. This leaves Ramatu all alone with the burden
      of her family’s responsibilities. Unfortunately, she lost all her working
      capital during the Covid-19 pandemic. As a consequence of this, Ramatu
      needed financial assistance, however, because of her level of education
      this was unattainable. She became very worried as to how she would take
      care of her sick husband.
    </p>
    <p>
      As a hardworking woman who cares very much about her sick husband and
      always wants to put a smile on his face, she resorted to selling charcoal,
      which was not generating enough income to pay for vital medication.
    </p>
    <p>
      But it does not have to be this way. Women should be given the chance to
      have access to financial support, grow and start a business no matter
      their educational level. This is why C.A. Micro Credit is on a mission to
      provide easily accessible financial support to women so that: 'no bankable
      poor should be left behind.'
    </p>
    <p>
      C.A. Micro Credit offered her a loan of GHS1,500.00 ($100.00) to restart
      Ramatu's food vendor business. Today, Ramatu makes about GHS800.00
      ($54.00) income every month which is used to take care of her husband’s
      medication and buy food for her family. She has restored hope and
      happiness in her husband, which is improving her husband’s condition.
    </p>
    <p>
      Hopefully, one day she will be able to grow her food vendor business into
      a restaurant, employ people to work and earn some income as well as take
      good care of her family.
    </p>
    <p>
      To support the numerous women out there like Ramatu to achieve their
      dreams and support their families, we are planning to increase our loan
      size from GHS2000.00 ($150) to GHS5000.00 ($350) per client and our client
      base from 150 to 500 clients.
    </p>
