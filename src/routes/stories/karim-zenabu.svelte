    <h1>Karim Zenabu</h1>
    <p>
      Did you know that most women in polygamous marriages in Ghana are
      responsible for taking care of their own children?
    </p>
    <p>
      Meet Karim Zenabu, a busy mother of four children who lives in Garu in the
      Upper East region of Ghana. She is married with two co-wives. Ramatu is in
      the soya bean cake business and has been in this business for the past
      five years. She said her business was successful until the prices of goods
      and services went up, and she made little profit from the business due to
      inadequate working capital. As a polygamous married woman, it is her
      responsibility to take care of her children's school fees, hospital bills,
      and feed and clothe them. Zenabu said she applied for a loan from a formal
      bank to expand her business and increase her income level, but her loan
      request was not granted because she could not provide
      collateral/guarantors for the loan. She became worried about the future
      and almost lost hope. According to her, she could no longer carry out her
      family's responsibilities, which affected her relationship with her
      children.
    </p>
    <p>
      C.A. Micro-Credit came along the line and supported her with a loan of
      GHS1,000. She used GHS500 to buy soya beans, GHS245 to buy 12.5 liters of
      cooking oil, GHS200 to buy firewood, and GHS55 for other ingredients for
      her business. For a period of six months, she repaid her loan and had a
      net profit of GHS600 extra income compared to previously. She said the
      extra income was used to buy food, pay her children's school fees, and
      partly invest in her business.
    </p>
    <p>
      Zenabu is on her second loan cycle and doing very well in her soya bean
      cake business. She said her fears and worries about the future are a thing
      of the past. She said her children are now happy with her and call her
      "sweet mummy." She expressed how joyful she feels each time her children
      call her "sweet mummy."
    </p>
    <p>
      She plans to build a soya bean cake factory in the future and employ and
      train young girls who have dropped out of school to have a hand in work
      and work their way out of poverty and be financially independent. She said
      she has not forgotten her dream to build a house for herself and her
      children so they can have a comfortable and peaceful home. Zenabu advises
      her colleagues to focus on reinvesting their profits in their businesses
      instead of using it to buy expensive clothes and jewelry. She believes
      that a woman's freedom and dignity depend on her level of income. Finally,
      she said, "I am most grateful to the Almighty God and C.A. Micro-Credit
      for restoring hope to me and my children. God bless C.A. Micro-Credit's
      investors."
    </p>
