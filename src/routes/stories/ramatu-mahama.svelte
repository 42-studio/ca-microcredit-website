    <h1>Mahama Ramatu</h1>
    <p>
      Mahama Ramatu is a busy and optimistic mother of seven children. Farming
      and petty trading are her sources of livelihood. She is married and lives
      in Garu, a town located in the Upper East region of Ghana, with her
      family. She said three of her children are in school while the rest have
      dropped out.
    </p>
    <p>
      Ramatu said she has been in the farming and petty trading business for
      over 30 years. She can recall that a company called DEKI came and
      supported them with loans to expand their businesses but left. Since then,
      she has not been able to acquire loans from formal banks because she has
      no collateral or guarantors to acquire a loan from formal banks. She said
      it affected her income level and family life badly. She could no longer
      support her children’s schooling, and because of that, four of her
      children had to drop out of school. Ramatu lamented bitterly how her
      children were bullied and disrespected by their schoolmates because they
      were not always in good school uniforms and sandals. Her husband is alive
      but does not support her to take care of her children. She said she has
      been taking care of the family for the past 28 years, and it has not been
      easy for her until she heard about C.A Micro Credit from a community
      member who is a customer of C.A Micro Credit. She said her friend advised
      her to enroll with C.A Micro Credit. She did and applied for a loan of
      GHS1,500, and it was approved for her. According to her, she used GHS400
      to buy fertilizer for her maize farm, GHS150 to hire labor, GHS800 to buy
      gari, sugar, biscuits, pencils, and so on to sell, and GHS50 as
      transportation costs.
    </p>
    <p>
      She said for a period of eight months, she repaid her loan, including the
      loan interest, and had a profit of GHS1,250. The extra income generated
      was used to send three of her children back to school and buy clothes and
      sandals for them. In addition, she had a good harvest from her maize farm,
      which is used to feed the family. Ramatu is on her third loan cycle, and
      she is optimistic that the profit that would be realized from her business
      would be able to pay her third loan and take care of her family,
      especially her children’s school fees.
    </p>
    <p>
      Ramatu said her tears and those of her children have been completely wiped
      off. She now has a happy family and is hopeful that, with financial
      support from C.A Microcredit and hard work, she would be able to stock her
      store fully and train her children in school so she can enjoy the fruits
      of her labor when she finally ages. Ramatu wants to advise her friends to
      enroll with C.A Micro Credit to get easy and reliable credit assistance to
      grow their businesses. They should work hard and manage their credit very
      well because credit management is very important in the successful running
      of any business.
    </p>
    <p>
      She wants to use this opportunity to show her deepest appreciation to C.A
      micro-credit and its investors for their support because C.A Micro Credit
      has put smiles on the faces of women and children in Garu, and she is not
      an exception. To the best of her knowledge, there are so many women out
      here who are struggling to work their way out of poverty and need the
      financial support of C.A Micro Credit.
    </p>
